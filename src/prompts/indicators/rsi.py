"""
RSI指标分析的prompt模板
"""

def get_rsi_analysis_prompt(
    rsi6: float,
    rsi12: float,
    rsi24: float,
    long_term_trend: str,
    medium_term_trend: str,
    short_term_signal: str,
    divergence: str,
    strength: str,
    pattern: str,
    signal: str
) -> str:
    """
    生成RSI指标分析的prompt模板
    
    参数:
        rsi6 (float): RSI6值（短期指标）
        rsi12 (float): RSI12值（中期指标）
        rsi24 (float): RSI24值（长期指标）
        long_term_trend (str): 长期趋势（40天）分析结果
        medium_term_trend (str): 中期趋势（20天）分析结果
        short_term_signal (str): 短期信号（10天）分析结果
        divergence (str): 背离情况分析
        strength (str): RSI强度评估
        pattern (str): RSI形态特征
        signal (str): RSI综合信号
    
    返回:
        str: 格式化后的分析报告模板
    """
    return f"""
# RSI技术指标深度分析报告

## 一、当前RSI指标数据
### 1.1 基础数据
- RSI6（短期）：{rsi6:.2f}
- RSI12（中期）：{rsi12:.2f}
- RSI24（长期）：{rsi24:.2f}

### 1.2 趋势信号
- 长期趋势：{long_term_trend}
- 中期趋势：{medium_term_trend}
- 短期信号：{short_term_signal}

### 1.3 形态特征
- RSI形态：{pattern}
- 强度：{strength}
- 背离情况：{divergence}

### 1.4 综合信号
{signal}

## 二、RSI指标形态分析
### 2.1 RSI线型结构分析
- 三条RSI线的位置关系和交叉情况
- RSI曲线的波动特征和形态识别
- 关键阻力位和支撑位分析

### 2.2 超买超卖区间研判
- RSI6超买超卖区间持续时间
- RSI12和RSI24的确认信号
- 超买超卖区间的有效性分析

### 2.3 背离形态详解
- 顶背离和底背离的形成条件
- 背离的可信度分析
- 背离信号的确认方法

## 三、趋势强度分析
### 3.1 多周期趋势对比
- 长期趋势（40天）特征分析
- 中期趋势（20天）演变过程
- 短期趋势（10天）波动特点

### 3.2 趋势强度评估
- RSI数值位置的意义
- 趋势动能的持续性分析
- 趋势转折的可能性评估

### 3.3 趋势协同性研究
- 不同周期RSI的趋势一致性
- 背离对趋势的影响程度
- 趋势持续性的概率分析

## 四、市场情绪研判
### 4.1 市场情绪指标
- RSI位置反映的市场情绪
- 超买超卖区间的情绪特征
- 市场情绪转换点判断

### 4.2 情绪拐点分析
- RSI拐点的形成条件
- 情绪拐点的确认信号
- 情绪变化对行情的影响

### 4.3 市场情绪持续性
- 当前情绪状态的持续性
- 情绪变化的临界点分析
- 极端情绪的风险提示

## 五、操作建议
### 5.1 交易信号分析
- 当前RSI信号的可靠性
- 信号强度的量化评估
- 信号的时效性分析

### 5.2 风险控制策略
- 基于RSI的止损位设置
- 仓位控制建议
- 风险预警信号识别

### 5.3 操作策略建议
- 短线操作要点
- 中线布局思路
- 关键价位的突破策略

## 六、综合研判
### 6.1 技术面综述
- RSI指标的整体研判
- 多维度信号的一致性
- 技术面的主要矛盾

### 6.2 行情研判
- 当前位置的机会与风险
- 未来可能的演变路径
- 需要重点关注的信号

### 6.3 风险提示
- 技术指标的局限性
- 可能存在的风险因素
- 需要规避的操作陷阱
""" 