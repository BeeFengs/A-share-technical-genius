"""
布林带指标分析的prompt模板
"""

def get_boll_analysis_prompt(
    upper: float,
    mid: float,
    lower: float,
    long_term_trend: str,
    medium_term_trend: str,
    short_term_signal: str,
    bandwidth: str,
    pattern: str,
    strength: str,
    signal: str
) -> str:
    """
    生成布林带指标分析的prompt模板
    
    参数:
        upper (float): 布林带上轨值
        mid (float): 布林带中轨值（20日移动平均线）
        lower (float): 布林带下轨值
        long_term_trend (str): 长期趋势（40天）分析结果
        medium_term_trend (str): 中期趋势（20天）分析结果
        short_term_signal (str): 短期信号（10天）分析结果
        bandwidth (str): 带宽状态分析
        pattern (str): 布林带形态特征
        strength (str): 趋势强度评估
        signal (str): 布林带综合信号
    
    返回:
        str: 格式化后的分析报告模板
    """
    return f"""
# 布林带(BOLL)技术指标深度分析报告

## 一、当前BOLL指标数据
### 1.1 基础数据
- 上轨：{upper:.2f}
- 中轨：{mid:.2f}
- 下轨：{lower:.2f}

### 1.2 趋势信号
- 长期趋势：{long_term_trend}
- 中期趋势：{medium_term_trend}
- 短期信号：{short_term_signal}

### 1.3 形态特征
- 带宽状态：{bandwidth}
- 形态特征：{pattern}
- 趋势强度：{strength}

### 1.4 综合信号
{signal}

## 二、布林带形态分析
### 2.1 带宽分析
- 当前带宽状态评估
- 带宽变化趋势分析
- 波动率周期特征

### 2.2 价格位置分析
- 价格在带中的相对位置
- 关键突破点位判断
- 轨道支撑压力分析

### 2.3 轨道形态研判
- 轨道扩张/收缩特征
- 轨道平行度分析
- 轨道斜率变化

## 三、趋势强度分析
### 3.1 多周期趋势对比
- 长期趋势（40天）特征
- 中期趋势（20天）演变
- 短期趋势（10天）波动

### 3.2 趋势强度评估
- 中轨斜率分析
- 价格突破强度
- 趋势持续性预判

### 3.3 趋势转折研判
- 潜在转折信号识别
- 趋势背离分析
- 转折概率评估

## 四、市场波动特征
### 4.1 波动率分析
- 带宽周期性特征
- 波动强度评估
- 波动趋势预测

### 4.2 突破特征分析
- 突破有效性判断
- 假突破风险评估
- 突破后续空间预判

### 4.3 均值回归分析
- 价格回归概率
- 回归目标位预测
- 回归时间周期估算

## 五、操作策略建议
### 5.1 交易信号分析
- 当前交易信号解读
- 信号可靠性评估
- 信号时效性分析

### 5.2 风险控制策略
- 止损位设置建议
- 仓位控制方案
- 风险预警信号

### 5.3 交易策略制定
- 短线操作建议
- 中线布局思路
- 关键价位部署

## 六、综合研判
### 6.1 技术面评估
- BOLL指标整体研判
- 多维度信号一致性
- 技术面关键矛盾

### 6.2 行情研判
- 当前位置机会与风险
- 未来可能演变路径
- 关键观察信号点

### 6.3 风险提示
- 指标局限性说明
- 潜在风险因素
- 防范措施建议
""" 